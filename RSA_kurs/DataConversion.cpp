octet_string toOctetString(const std::string str) {
    octet_string os;
    std::string::const_iterator it = str.begin();
    while (it != str.end()) {
        os.push_back(static_cast<uint8_t>(*it++));
    }
    return os;
}

std::string toNormalString(const octet_string str) {
    std::string result;
    octet_string::const_iterator it = str.begin();
    while (it != str.end()) {
        result.push_back((char)*it++);
    }
    return result;
}

mpz_class OS2IP(const octet_string os) {
    mpz_class result = 0;
    size_t i = 0, s = os.size();
    mpz_class temp;
    octet_string::const_iterator it;

    for (it = os.begin(); it != os.end(); it++) {
        mpz_ui_pow_ui(temp.get_mpz_t(), 256, s - i - 1);
        result += ((unsigned int)*it) * temp;
        i++;
    }
    return result;
}

octet_string I2OSP(const mpz_class n, size_t length) {
    size_t i;
    mpz_class temp, q, r;
    octet_string result;
    mpz_ui_pow_ui(temp.get_mpz_t(), 256, length);
    if (n >= temp) {
        return result;
    }

    temp = 0;
    i = 0;
    q = n;

    while (q >= 256) {
        mpz_tdiv_qr_ui(q.get_mpz_t(), r.get_mpz_t(), q.get_mpz_t(), 256);
        result.insert(result.begin(),r.get_ui());
        i++;
    }
    result.insert(result.begin(), q.get_ui());
    return result;
}

